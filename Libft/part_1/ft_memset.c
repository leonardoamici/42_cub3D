#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	x;
	char	*pt;

	x = 0;
	pt = str;
	while (x < n)
	{
		pt[x] = c;
		x++;
	}
	return (str);
}
