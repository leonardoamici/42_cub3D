#include "libft.h"

int	ft_string_fd(int fd, char *str)
{
	int	x;

	x = 0;
	if (str == 0)
	{
		write(fd, "(null)", 6);
		return (6);
	}
	while (str[x] != '\0')
	{
		write(fd, &str[x], 1);
		x++;
	}
	return (x);
}
